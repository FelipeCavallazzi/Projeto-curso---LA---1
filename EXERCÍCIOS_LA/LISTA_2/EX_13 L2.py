num = int(input("Digite um número de 1 a 7: "))

if num == 1:
    dia = "1-Domingo"
if num == 2:
    dia = "2-Segunda"
if num == 3:
    dia = "3-Terça"
if num == 4:
    dia = "4-Quarta"
if num == 5:
    dia = "5-Quinta"
if num == 6:
    dia = "6-Sexta :)"
if num == 7:
    dia = "7-Sábado"

else:
    dia = ("Valor inválido")

print(dia)
